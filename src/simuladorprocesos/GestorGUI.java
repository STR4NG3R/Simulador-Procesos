    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (checkAleatorio.isSelected()) {
            serviciosBack.add(new GenerarServicios().generateServicio(
                    serviciosBack.size()));
           listProcess.addElement(serviciosBack.get(serviciosBack.size() - 1).getName());
           barrs.setData();
        } else {
            boolean flag = true;
            String nombre = txtNombre.getText();
            int duracion = -1;
            if (!txtDuracion.getText().equals("")) {
                duracion = Integer.parseInt(txtDuracion.getText());
            }
            int prioridad = comboPrioridad.getSelectedIndex();
            switch (prioridad) {
                case 0:
                    prioridad = 1;
                    break;
                case 1:
                    prioridad = 5;
                    break;
                case 2:
                    prioridad = 10;
                    break;
            }
            if (nombre.equals("")) {
                JOptionPane.showMessageDialog(null, "Error, el nombre no puede ser nulo");
                flag = false;
            }
            if (duracion == 0) {
                JOptionPane.showMessageDialog(null, "¬øTiene alg√∫n sentido un proceso sin duraci√≥n?");
                flag = false;
            }
            if (duracion == -1) {
                JOptionPane.showMessageDialog(null, "Campo duraci√≥n vacio");
                flag = false;
            }
            if (flag) {
                serviciosBack.add(new GenerarServicios().generateServicio(nombre, duracion, prioridad,
                        serviciosBack.size()));
                listProcess.addElement(serviciosBack.get(serviciosBack.size() - 1).getName());
                barrs.setData();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

